function ratings(
  overall: DistrictRatings['overall'],
  summary: string,
  metrics: DistrictMetricsDraft,
): DistrictMetadataDraft['ratings'] {
  return {
    overall,
    summary,
    metrics,
  }
}

function district(
  config: Omit<DistrictMetadataDraft, 'zoneType' | 'boundaryNote'> &
    Partial<Pick<DistrictMetadata, 'zoneType' | 'boundaryNote'>>,
): DistrictMetadata {
  const amenities =
    config.ratings.metrics.amenities ?? amenityScoresById[config.id] ?? 3
  const generatedParks = greenScoresById[config.id]
  const lifestyleTags = districtLifestyleTagsById[config.id] ?? []
  const parks =
    lifestyleTags.includes('coastal') && typeof generatedParks === 'number' && generatedParks < 3
      ? 3
      : (generatedParks ?? config.ratings.metrics.parks ?? 3)

  return {
    zoneType: 'legacy-district',
    boundaryNote: 'Curated Dublin postal area boundary aligned to the open postcode map.',
    ...config,
    ratings: {
      ...config.ratings,
      metrics: {
        ...config.ratings.metrics,
        amenities,
        parks,
      },
    },
  }
}

